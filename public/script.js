// ===== CART =====
function addToCart() {
  if (!currentProduct) return;
  const existing = findCartLine(currentProduct.id, selectedSize);
  // Stock is per product, so all sizes of this product share the same stock.
  const productQtyInCart = cart.reduce(
    (s, c) => s + (sameId(c.id, currentProduct.id) ? c.qty : 0),
    0,
  );
  if (currentQty + productQtyInCart > currentProduct.stock) {
    showToast(i18n[currentLang]["toast.out_of_stock"]);
    return;
  }
  if (existing) {
    existing.qty += currentQty;
  } else {
    cart.push({ ...currentProduct, qty: currentQty, size: selectedSize });
  }
  updateCart();
  setModalActionState("remove");
  showToast(i18n[currentLang]["toast.added"]);
  closeModalDirect();
}

function removeFromCart() {
  if (!currentProduct) return;
  cart = cart.filter(
    (c) => !(sameId(c.id, currentProduct.id) && (c.size || null) === (selectedSize || null)),
  );
  updateCart();
  setModalActionState("add");
  showToast(i18n[currentLang]["toast.removed"]);
}

function updateCart() {
  normalizeCartItems();
  try { localStorage.setItem('seva_cart', JSON.stringify(cart)); } catch(e) {}

  var total = cart.reduce(function(s, c) { return s + c.qty; }, 0);

  var badge = document.getElementById("cartBadge");
  if (badge) badge.textContent = total;

  var fabBadge = document.getElementById("cartFabBadge");
  if (fabBadge) {
    fabBadge.textContent = total;
    fabBadge.classList.toggle("has-items", total > 0);
  }

  const itemsEl = document.getElementById("cartItems");
  const footerEl = document.getElementById("cartFooter");

  const t = i18n[currentLang];
  if (cart.length === 0) {
    itemsEl.innerHTML = `<div class="cart-empty"><div class="cart-empty-icon">🛒</div><p>${t["cart.empty"]}</p></div>`;
    footerEl.style.display = "none";
  } else {
    // Group lines by product so the same item shows once; each size line is removable on its own.
    const groups = [];
    const groupIndex = {};
    cart.forEach((item) => {
      const key = String(item.id);
      if (groupIndex[key] === undefined) {
        groupIndex[key] = groups.length;
        groups.push({ id: item.id, name: item.name, img: item.img, price: item.price, qty: 0, lines: [] });
      }
      const g = groups[groupIndex[key]];
      g.qty += item.qty;
      g.lines.push({ size: item.size, qty: item.qty });
    });

    itemsEl.innerHTML = groups
      .map((g) => {
        const lines = g.lines
          .map(
            (line) => `
          <div class="cart-line">
            <span class="cart-line-meta">${line.size ? t["cart.size"] + ": " + line.size + " · " : ""}${t["cart.qty"]}: ${line.qty} — ${(g.price * line.qty).toLocaleString("ar-EG")} ${t["currency"]}</span>
            <button class="cart-line-remove" type="button" onclick='removeCartLine(${jsString(g.id)}, ${jsString(line.size || "")})' aria-label="${t["modal.remove"]}">✕</button>
          </div>`,
          )
          .join("");
        const totalRow =
          g.lines.length > 1
            ? `<div class="cart-item-price">${t["cart.qty"]}: ${g.qty} · ${(g.price * g.qty).toLocaleString("ar-EG")} ${t["currency"]}</div>`
            : "";
        return `
      <div class="cart-item">
        <img class="cart-item-img" src="${g.img}" alt="${g.name}" onerror="handleImageError(this)">
        <div class="cart-item-info">
          <div class="cart-item-name">${g.name}</div>
          ${lines}
          ${totalRow}
        </div>
      </div>
    `;
      })
      .join("");
    const grandTotal = cart.reduce((s, c) => s + c.price * c.qty, 0);
    document.getElementById("cartTotal").textContent =
      grandTotal.toLocaleString("ar-EG") + " " + t["currency"];
    footerEl.style.display = "";
  }

  // Re-render grids to update card states
  populateGrids();
}

