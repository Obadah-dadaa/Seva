function applyFilters() {
  var sort = document.getElementById('sortSelect').value;
  activeFilterSize = document.getElementById('sizeSelect').value;

  var filtered = products.filter(function(p) {
    var catOk  = !activeFilterCat  || p.cat === activeFilterCat;
    var sizeOk = !activeFilterSize || (p.sizes && p.sizes.indexOf(activeFilterSize) !== -1);
    return catOk && sizeOk;
  });

  if (sort === 'price-asc')  filtered = filtered.slice().sort(function(a, b) { return a.price - b.price; });
  if (sort === 'price-desc') filtered = filtered.slice().sort(function(a, b) { return b.price - a.price; });
  if (sort === 'newest')     filtered = filtered.slice().sort(function(a, b) { return b.id - a.id; });

  var grid = document.getElementById('allGrid');
  if (filtered.length === 0) {
    grid.innerHTML = '<div class="filter-no-results">لا توجد منتجات تطابق الفلتر المحدد</div>';
  } else {
    grid.innerHTML = filtered.map(renderCard).join('');
  }
}

