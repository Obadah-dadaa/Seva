async function submitOrder(paymentMethod, paymentReference = null) {
  if (isSubmittingOrder) return;
  isSubmittingOrder = true;
  const customer = getCheckoutCustomer();
  const payload = {
    customer,
    payment_method: paymentMethod,
    payment_reference: paymentReference,
    items: cart.map((item) => ({
      id: item.id,
      qty: item.qty,
      size: item.size,
    })),
  };

  try {
    const response = await fetch(window.SEVA_ORDER_STORE_URL, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Accept: "application/json",
        "X-CSRF-TOKEN": document.querySelector('meta[name="csrf-token"]').content,
      },
      body: JSON.stringify(payload),
    });

    const result = await response.json().catch(() => ({}));

    if (!response.ok) {
      showToast(result.message || i18n[currentLang]["checkout.failed"]);
      isSubmittingOrder = false;
      return;
    }

    cart = [];
    updateCart();
    closeCheckout();
    closeCart();
    const isPendingPayment = paymentMethod !== 'cash_on_delivery';
    showToast(i18n[currentLang][isPendingPayment ? 'checkout.pending' : 'checkout.success']);
    try { localStorage.setItem('seva_last_order', JSON.stringify({ orderNumber: result.order_number, trackUrl: result.track_url })); } catch(e) {}
    setTimeout(function() { window.location.href = result.track_url; }, 1400);
  } catch (error) {
    isSubmittingOrder = false;
    showToast(i18n[currentLang]["checkout.failed"]);
  }
}

function buildOrderWhatsAppMessage(orderNumber, cartSnapshot, customer, paymentMethod) {
  const t = i18n[currentLang];
  const paymentLabels = {
    cash_on_delivery: t["checkout.cod"],
    vodafone_cash: t["checkout.vodafone"],
    instapay: t["checkout.instapay"],
  };
  const lines = cartSnapshot.map(item => {
    const size = item.size ? ` - ${t["cart.size"]} ${item.size}` : '';
    return `• ${item.name}${size} × ${item.qty} = ${(item.price * item.qty).toLocaleString()} ${t.currency}`;
  });
  const total = cartSnapshot.reduce((sum, i) => sum + i.price * i.qty, 0);
  return [
    `مرحباً SEVA 👋`,
    ``,
    `✅ رقم الطلب: ${orderNumber}`,
    ``,
    `📦 المنتجات:`,
    ...lines,
    ``,
    `💰 الإجمالي: ${total.toLocaleString()} ${t.currency}`,
    `💳 الدفع: ${paymentLabels[paymentMethod] || paymentMethod}`,
    ``,
    `👤 ${customer.name}`,
    `📞 ${customer.phone}`,
    `📍 ${customer.city} - ${customer.address}`,
  ].join('\n');
}

function showOrderSuccess(orderNumber, trackUrl, cartSnapshot, customer, paymentMethod) {
  const t = i18n[currentLang];
  const isPending = paymentMethod !== 'cash_on_delivery';
  document.getElementById('successTitle').textContent = t[isPending ? 'success.title.pending' : 'success.title'];
  document.getElementById('successOrderNum').textContent = `${t['success.order_num']} ${orderNumber}`;
  const trackBtn = document.getElementById('successTrackBtn');
  trackBtn.href = trackUrl;
  trackBtn.textContent = t['success.track'];
  const msg = buildOrderWhatsAppMessage(orderNumber, cartSnapshot, customer, paymentMethod);
  const waBtn = document.getElementById('successWhatsAppBtn');
  waBtn.href = `https://wa.me/${whatsappNumber}?text=${encodeURIComponent(msg)}`;
  waBtn.textContent = t['success.whatsapp'];
  document.getElementById('successCloseBtn').textContent = t['success.close'];
  localStorage.setItem('seva_last_order', JSON.stringify({ orderNumber, trackUrl }));
  document.getElementById('orderSuccessModal').classList.add('open');
}

