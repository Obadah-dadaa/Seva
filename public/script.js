function handleImageError(img) {
  if (!img) return;
  img.onerror = null;
  img.classList.add("image-fallback");
  img.src = window.SEVA_PLACEHOLDER_IMAGE || "/seva-logo-transparent.png";
}

function normalizeCartItems() {
  if (!Array.isArray(cart)) {
    cart = [];
    return;
  }

  cart = cart
    .map(function(item) {
      const product = products.find((p) => sameId(p.id, item && item.id));
      if (!product || product.stock <= 0) return null;

      const qty = Math.max(1, parseInt(item.qty, 10) || 1);
      return {
        ...product,
        qty: Math.min(qty, product.stock),
        size: item.size || null,
      };
    })
    .filter(Boolean);
}

function setModalActionState(state) {
  const addBtn = document.getElementById("btnAddCart");
  const removeBtn = document.getElementById("btnRemoveCart");
  const actions = document.getElementById("modalActions");
  if (!addBtn || !removeBtn) return;

  if (state === "hidden") {
    addBtn.style.display = "none";
    removeBtn.style.display = "none";
    if (actions) actions.style.display = "none";
    return;
  }

  if (actions) actions.style.display = "flex";
  addBtn.style.display = state === "remove" ? "none" : "inline-flex";
  removeBtn.style.display = state === "remove" ? "inline-flex" : "none";
}

