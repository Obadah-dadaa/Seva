function sameId(a, b) {
  return String(a) === String(b);
}

// A cart line is identified by product id AND size, so the same product can
// be added in multiple sizes as separate lines.
function findCartLine(id, size) {
  return cart.find((c) => sameId(c.id, id) && (c.size || null) === (size || null));
}

// Refresh the modal add/remove button based on the currently selected size.
function refreshModalAction() {
  if (!currentProduct) return;
  if (currentProduct.stock === 0) {
    setModalActionState("hidden");
    return;
  }
  setModalActionState(findCartLine(currentProduct.id, selectedSize) ? "remove" : "add");
}

function jsString(value) {
  return JSON.stringify(String(value));
}

function handleImageError(img) {
  if (!img) return;
  img.onerror = null;
  img.classList.add("image-fallback");
  img.src = window.SEVA_PLACEHOLDER_IMAGE || "/seva-logo-transparent.png";
}

function normalizeCartItems() {
  if (!Array.isArray(cart)) {
    cart = [];
    return;
  }

  cart = cart
    .map(function(item) {
      const product = products.find((p) => sameId(p.id, item && item.id));
      if (!product || product.stock <= 0) return null;

      const qty = Math.max(1, parseInt(item.qty, 10) || 1);
      return {
        ...product,
        qty: Math.min(qty, product.stock),
        size: item.size || null,
      };
    })
    .filter(Boolean);
}

function setModalActionState(state) {
  const addBtn = document.getElementById("btnAddCart");
  const removeBtn = document.getElementById("btnRemoveCart");
  const actions = document.getElementById("modalActions");
  if (!addBtn || !removeBtn) return;

  if (state === "hidden") {
    addBtn.style.display = "none";
    removeBtn.style.display = "none";
    if (actions) actions.style.display = "none";
    return;
  }

  if (actions) actions.style.display = "flex";
  addBtn.style.display = state === "remove" ? "none" : "inline-flex";
  removeBtn.style.display = state === "remove" ? "inline-flex" : "none";
}

// ===== RENDER PRODUCT CARD =====
function renderCard(p) {
  const t = i18n[currentLang];
  const outOfStock = p.stock === 0;
  const productId = jsString(p.id);
  return `
    <div class="product-card${outOfStock ? ' card-out-of-stock' : ''}" ${outOfStock ? '' : `onclick='openModal(${productId})'`}>
      <div class="product-img-wrap">
        <img src="${p.img}" alt="${p.name}" loading="lazy" onerror="handleImageError(this)">
        ${outOfStock
          ? `<div class="out-of-stock-overlay">${t["card.out_of_stock"] || "نفذت الكمية"}</div>`
          : `<div class="product-overlay"><button class="overlay-btn" onclick='event.stopPropagation();openModal(${productId})'>${t["card.details"]}</button></div>`
        }
        ${p.discount ? `<span class="badge-discount">${p.discount}% ${t["card.off"]}</span>` : ""}
        <span class="badge-cat">${p.cat}</span>
      </div>
      <div class="product-info">
        <div class="product-name">${p.name}</div>
        <div class="product-type">${p.type}</div>
        <div class="product-pricing">
          ${p.oldPrice ? `<span class="price-old">${p.oldPrice.toLocaleString("ar-EG")} ${t["currency"]}</span>` : ""}
          <span class="price-new">${p.price.toLocaleString("ar-EG")} ${t["currency"]}</span>
        </div>
        ${
          p.sizes.length > 0
            ? `
        <div class="sizes">
          ${p.sizes
            .slice(0, 4)
            .map((s) => `<span class="size-tag">${s}</span>`)
            .join("")}
          ${p.sizes.length > 4 ? `<span class="size-tag">+${p.sizes.length - 4}</span>` : ""}
        </div>`
            : ""
        }
      </div>
    </div>`;
}

function renderPreorderCard(p) {
  const t = i18n[currentLang];
  const preorderId = jsString(p.id);
  return `
    <div class="preorder-card">
      <div class="preorder-img-wrap">
        <img src="${p.img}" alt="${p.name}" loading="lazy" onerror="handleImageError(this)">
        <span class="preorder-pill">${p.type || t["nav.preorders"]}</span>
      </div>
      <div class="preorder-info">
        <h3>${p.name}</h3>
        <p>${p.description || ""}</p>
        <div class="preorder-meta">
          ${p.priceNote ? `<span>${t["preorder.price"]}: ${p.priceNote}</span>` : ""}
          ${p.estimatedDelivery ? `<span>${t["preorder.delivery"]}: ${p.estimatedDelivery}</span>` : ""}
        </div>
        <button class="btn-whatsapp" onclick='openPreorderWhatsApp(${preorderId})'>${t["preorder.whatsapp"]}</button>
      </div>
    </div>`;
}

function renderPreorderGrid(elementId, list) {
  const el = document.getElementById(elementId);
  if (!el) return;

  if (!list.length) {
    el.innerHTML = `<p class="cart-empty">${i18n[currentLang]["preorder.empty"]}</p>`;
    return;
  }

  el.innerHTML = list.map(renderPreorderCard).join("");
}

function openPreorderWhatsApp(id) {
  const product = preorderProducts.find((p) => sameId(p.id, id));
  if (!product) return;

  const message = [
    "مرحبا SEVA، أريد الاستفسار عن طلب وصاية:",
    product.name,
    product.type ? "النوع: " + product.type : "",
    product.priceNote ? "ملاحظة السعر: " + product.priceNote : "",
    product.estimatedDelivery ? "مدة التنفيذ: " + product.estimatedDelivery : "",
    product.img ? "\n" + product.img : "",
  ]
    .filter(Boolean)
    .join("\n");

  window.open(
    `https://wa.me/${whatsappNumber}?text=${encodeURIComponent(message)}`,
    "_blank",
  );
}

function scrollPreorderSlider(sliderId, direction) {
  const slider = document.getElementById(sliderId);
  if (!slider) return;

  const card = slider.querySelector(".preorder-card");
  const amount = card ? card.getBoundingClientRect().width + 18 : 420;
  const dirMultiplier = document.documentElement.dir === "rtl" ? -1 : 1;
  slider.scrollBy({
    left: direction * amount * dirMultiplier,
    behavior: "smooth",
  });
}

function resetPreorderSliders() {
  ["homePreorderGrid", "allPreorderGrid"].forEach((sliderId) => {
    const slider = document.getElementById(sliderId);
    if (!slider) return;

    slider.scrollLeft = document.documentElement.dir === "rtl"
      ? slider.scrollWidth
      : 0;
  });
}

// ===== POPULATE GRIDS =====
// Hide a home-page section (header + grid) when it has no products to show.
function toggleSectionByGrid(gridId, hasItems) {
  const grid = document.getElementById(gridId);
  if (!grid) return;
  const section = grid.closest("section");
  if (section) section.style.display = hasItems ? "" : "none";
}

function populateGrids() {
  const all = products;
  const featured = products.filter((p) => p.featured);

  document.getElementById("homeGrid").innerHTML = featured
    .slice(0, 8)
    .map(renderCard)
    .join("");
  document.getElementById("latestGrid").innerHTML = all
    .slice(0, 8)
    .map(renderCard)
    .join("");
  document.getElementById("allGrid").innerHTML = all.map(renderCard).join("");
  renderPreorderGrid("homePreorderGrid", preorderProducts);
  renderPreorderGrid("allPreorderGrid", preorderProducts);

  // Hide home-page sections that have no content to show.
  toggleSectionByGrid("homeGrid", featured.length > 0);
  toggleSectionByGrid("latestGrid", all.length > 0);
  toggleSectionByGrid("homePreorderGrid", preorderProducts.length > 0);
}

