function openTrackModal() {
  const input = document.getElementById('trackOrderInput');
  const saved = localStorage.getItem('seva_last_order');
  if (saved && input) {
    try {
      const { orderNumber } = JSON.parse(saved);
      if (orderNumber) input.value = orderNumber;
    } catch (e) {}
  }
  document.getElementById('trackSearchModal').classList.add('open');
  setTimeout(() => { if (input) input.focus(); }, 150);
}

function closeTrackModal(e) {
  if (!e || e.target === document.getElementById('trackSearchModal')) {
    document.getElementById('trackSearchModal').classList.remove('open');
  }
}

