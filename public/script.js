var activeFilterCat  = '';
var activeFilterSize = '';

function initFilters() {
  var cats = [];
  products.forEach(function(p) { if (p.cat && cats.indexOf(p.cat) === -1) cats.push(p.cat); });
  var catsEl = document.getElementById('filterCats');
  cats.forEach(function(cat) {
    var btn = document.createElement('button');
    btn.className = 'filter-cat-btn';
    btn.textContent = cat;
    btn.onclick = function() { setFilterCat(this, cat); };
    catsEl.appendChild(btn);
  });

  var sizes = [];
  products.forEach(function(p) {
    (p.sizes || []).forEach(function(s) { if (s && sizes.indexOf(s) === -1) sizes.push(s); });
  });
  var sizeEl = document.getElementById('sizeSelect');
  sizes.forEach(function(s) {
    var opt = document.createElement('option');
    opt.value = s; opt.textContent = s;
    sizeEl.appendChild(opt);
  });
}

function setFilterCat(btn, cat) {
  activeFilterCat = cat;
  document.querySelectorAll('.filter-cat-btn').forEach(function(b) { b.classList.remove('active'); });
  btn.classList.add('active');
  applyFilters();
}

function applyFilters() {
  var sort = document.getElementById('sortSelect').value;
  activeFilterSize = document.getElementById('sizeSelect').value;

  var filtered = products.filter(function(p) {
    var catOk  = !activeFilterCat  || p.cat === activeFilterCat;
    var sizeOk = !activeFilterSize || (p.sizes && p.sizes.indexOf(activeFilterSize) !== -1);
    return catOk && sizeOk;
  });

  if (sort === 'price-asc')  filtered = filtered.slice().sort(function(a, b) { return a.price - b.price; });
  if (sort === 'price-desc') filtered = filtered.slice().sort(function(a, b) { return b.price - a.price; });
  if (sort === 'newest')     filtered = filtered.slice().sort(function(a, b) { return b.id - a.id; });

  var grid = document.getElementById('allGrid');
  if (filtered.length === 0) {
    grid.innerHTML = '<div class="filter-no-results">لا توجد منتجات تطابق الفلتر المحدد</div>';
  } else {
    grid.innerHTML = filtered.map(renderCard).join('');
  }
}

