function doSearch() {
  const q = document.getElementById("searchInput").value.trim().toLowerCase();
  const res = document.getElementById("searchResults");
  if (!q) {
    res.innerHTML = "";
    return;
  }
  const matches = products.filter(
    (p) => p.name.includes(q) || p.cat.includes(q) || p.type.includes(q),
  );
  if (matches.length === 0) {
    res.innerHTML = `<p style="text-align:right;color:var(--muted);padding:12px;font-size:14px">${i18n[currentLang]["search.empty"]}</p>`;
    return;
  }
  res.innerHTML = matches
    .map(
      (p) => `
    <div class="search-result-item" onclick='document.getElementById("searchOverlay").classList.remove("open");openModal(${jsString(p.id)})'>
      <div class="search-result-info">
        <div class="search-result-name">${p.name}</div>
        <div class="search-result-price">${p.price.toLocaleString("ar-EG")} ج.م</div>
      </div>
      <img class="search-result-img" src="${p.img}" alt="${p.name}" onerror="handleImageError(this)">
    </div>
  `,
    )
    .join("");
}

